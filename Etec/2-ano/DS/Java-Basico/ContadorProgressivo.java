public class ContadorProgressivo {
    public static void main(String[] args) {
        // Caixa de execução

        for (int i = 0; i < 10; i++) {
            /* Comando for(para). Variavel i recebe o valor inicial 10, não pode ser
            maior que 10 e deve receber o valor de todos os números até 10
            */

            System.out.print(i + " ");
            // Exime na tela os números armazenados no i, com espaços
        }
        System.out.println("\nAcabou!");
        // Exibe na tela que a execução acabou
    }
}
